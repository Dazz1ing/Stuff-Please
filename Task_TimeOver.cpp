	Resource::WP  Resource::instance;
	//-------------------------------------------------------------------
	//リソースの初期化
	bool  Resource::Initialize()
	{
		this->img = DG::Image::Create("./data/image/timeover.png");
		return true;
	}
	//-------------------------------------------------------------------
	//リソースの解放
	bool  Resource::Finalize()
	{
		this->img.reset();
		return true;
	}
	//-------------------------------------------------------------------
	//「初期化」タスク生成時に１回だけ行う処理
	bool  Object::Initialize()
	{
		//スーパークラス初期化
		__super::Initialize(defGroupName, defName, true);
		//リソースクラス生成orリソース共有
		this->res = Resource::Create();

		//★データ初期化
		this->posY = -300.f;
		this->render2D_Priority[1] = 0.01f;
		//★タスクの生成

		return  true;
	}
	//-------------------------------------------------------------------
	//「終了」タスク消滅時に１回だけ行う処理
	bool  Object::Finalize()
	{
		//★データ＆タスク解放


		if (!ge->QuitFlag() && this->nextTaskCreate) {
			//★引き継ぎタスクの生成
			auto r = Result::Object::Create(true);
		}

		return  true;
	}
	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		auto inp = ge->in1->GetState();
		if (inp.ST.down) {
			ge->KillAll_G("統括");
			this->Kill();
		}
		auto easingFunc = getEasingFunction(EaseOutBounce);
		this->posY = easingFunc(this->posY);
		
	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		//ML::Box2D draw(ge->screenWidth / 2, 0, 700 / 3, 420 / 3);
		ML::Box2D draw(ge->screenWidth / 2, ge->screenHeight / 2, 700 / 3, 420 / 3);
		//draw.Offset(0, this->posY);
		ML::Box2D src(0, 0, 700, 420);
		this->res->img->Draw(draw, src);
	}

